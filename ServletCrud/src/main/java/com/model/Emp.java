package com.model;


public class Emp {
	    private int id;
	    private String name, dept;

	    public int getId ()
	    {
	        return id;
	    }

	    public void setId (int id)
	    {
	        this.id = id;
	    }

	    public String getName ()
	    {
	        return name;
	    }

	    public void setName (String name)
	    {
	        this.name = name;
	    }

	    public String getDept ()
	    {
	        return dept;
	    }

	    public void setDept (String dept)
	    {
	        this.dept = dept;
	    }
	}

